class Solution(object):
    def strToInt(self, str):
        res = 0
        flag = False
        neg = False
        for c in str:
            if c == ' ' and not flag:
                continue
            flag = True
            if c == '-' or c == '+':
                neg = c == '-'
            elif '0' <= c <= '9':
                res = res * 10 + (ord(c) - ord('0'))
            else:
                break
        res = res if not neg else -res
        res = min((1 << 31) - 1, res)
        res = max(-(1 << 31), res)
        return res
