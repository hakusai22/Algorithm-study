"""
找出所有相加之和为 n 的 k 个数的组合，且满足下列条件：
只使用数字1到9
每个数字 最多使用一次 
返回 所有可能的有效组合的列表 。该列表不能包含相同的组合两次，组合可以以任何顺序返回。
输入: k = 3, n = 7
输出: [[1,2,4]]
"""

class Solution:
    def combinationSum3(self, k: int, n: int) -> List[List[int]]:
        ref = [i for i in range(1, 10)]
        res = []
        track = []

        def dfs(start, k, num_sum):
            if len(track) == k and n == num_sum:
                res.append(track[:])
                return
            if num_sum > n or len(track) > k:
                return
            for i in range(start, len(ref)):
                track.append(ref[i])
                num_sum += ref[i]
                dfs(i + 1, k, num_sum)  # i + 1 前面元素不能重复使用
                track.pop()
                num_sum -= ref[i]

        dfs(0, k, 0)
        return res
